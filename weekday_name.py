def weekday_name(day_of_week):
    if day_of_week == 1:
        return "Monday"
    elif day_of_week == 2:
        return "Tuesday"
    elif day_of_week == 3:
        return "Wednesday"
    elif day_of_week == 4:
        return "Thursday"
    elif day_of_week == 5:
        return "Friday"
    elif day_of_week == 6:
        return "Saturday"
    elif day_of_week == 7:
        return "Sunday"
    else:
        return "There is no day like that- you can only enter between 1-7"
