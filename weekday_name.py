def weekday_name(day_of_week):
    if(day_of_week == 1):
      print("Monday")
    elif day_of_week == 2:
        print("Tuesday")
    elif day_of_week == 3:
        print("Wednesday")
    elif day_of_week == 4:
        print("Thursday")
    elif day_of_week == 5:
        print("Friday")
    elif day_of_week == 6:
        print("Saturday")
    elif day_of_week == 7:
        print("Sunday")
    else:
      print("There is no day like that- you can only enter between 1-7") 